def graph(values):
    rows = values.split('\n')
    grid = []
    row_count = len(rows)
    row_length = len(rows[0])
    for row in range(row_count):
        grid.append([])
        for column in range(row_length):
            grid[row].append(rows[row][column])
    return grid

def filled_adjacent(grid, row, rows, column, columns):
    adj_rows = [row]
    adj_columns = [column]
    if row > 0:
        adj_rows.append(row - 1)
    if column > 0:
        adj_columns.append(column - 1)
    if row < rows - 1:
        adj_rows.append(row + 1)
    if column < columns - 1:
        adj_columns.append(column + 1)

    count = 0
    for adj_row in adj_rows:
        for adj_column in adj_columns:
            if adj_row != row and adj_column != column:
                if grid[adj_row][adj_column] == '1':
                    count += 1
    return count

def output_results(round, grid, rows, columns, outputs='L#.'):
    print()
    print("Results for round", round)
    for row in range(rows):
        for column in range(columns):
            value = grid[row][column]
            if value == '0':
                print(outputs[0], end='')
            if value == '1':
                print(outputs[1], end='')
            if value == '9':
                print(outputs[2], end='')
        print()

def solve(cases):
    rows = len(cases)
    columns = len(cases[0])
    current = cases[:]

    round = 0
    output_results(round, current, rows, columns)
    fills = True
    empties = True
    while fills or empties: #If any seat changes occur, continue
        round += 1
        fills = []
        empties = []
        for row in range(rows):
            for column in range(columns):
                cell = current[row][column]
                if cell == '9':
                    continue
                adjacents = filled_adjacent(current, row, rows, column, columns)
                print(cell,'at',row, column, adjacents)
                if cell == '1' and adjacents >= 4:
                    print('Leave seat', row, column)
                    empties.append((row, column))
                elif cell == '0' and adjacents == 0:
                    print('Fill  seat', row, column)
                    fills.append((row,column))
        
        for fill in fills:
            current[fill[0]][fill[1]] = '1'
        for empty in empties:
            current[empty[0]][empty[1]] = '0'
        output_results(round, current, rows, columns)
    
    count = 0
    for row in current:
        for value in row:
            if value == '1':
                count += 1

