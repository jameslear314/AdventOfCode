def solve(cases=TEST):
    forest = []
    for line in cases:
        trees = []
        for char in line:
            if char == '.':
                trees.append(0)
            else:
                trees.append(1)
        forest.append(trees)

    column = 0
    encountered = 0
    for line in range(len(forest)):
        if forest[line][column % len(forest[0])]:
            encountered += 1
        column += 3
    return encountered

if __name__ == '__main__':
    test_results = solve()
    print(test_results)
